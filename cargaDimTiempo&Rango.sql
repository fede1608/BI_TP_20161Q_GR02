WITH mycte AS
  (SELECT cast('2008-01-01' AS datetime) DateValue
   UNION ALL SELECT DateValue + 1
   FROM mycte
   WHERE DateValue + 1 < '2017-01-01')
INSERT INTO dbo.LK_TIEMPO (AÑO, MES, DIA_SEMANA, DIA)
SELECT Year(DateValue) AS AÑO,
       MONTH (DateValue) AS MES,
             datepart(dw, DateValue) AS DIA_SEMANA,
             DAY (DateValue) AS DIA
FROM mycte OPTION (MAXRECURSION 0);

